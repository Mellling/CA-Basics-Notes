// [ 클럭 ] ///////////////////////////////////////////////

// 클럭 속도가 높아지면 CPU는 명령어 사이클을 더 빠르게 반복한다.따라서 클럭 속도가 높은 CPU는 일반적으로 성능이 좋다.

// 클럭 속도는 1초에 클럭이 몇 번 반복되는지 나타내는 헤르츠(Hz) 단위로 측정한다. 1GHz는 10 ^ 9Hz.

// CPU는 계속 일정한 클럭 속도를 유지하기보다는 고성능을 요하는 순간에는 순간적으로 속도를 높이고, 그렇지 않을 때는 유연하게 클럭 속도를 낮춘다.

// 클럭 속도를 무지막지하게 높인다고 CPU가 무조건 빨라지진 않는다.← 클럭 속도를 무작정 높이면 발열 문제가 심각해지기 때문이다.

// 이처럼 클럭 속도를 높이면 CPU가 빨라지긴 하나, 클럭 속도만으로 CPU의 성능을 높이는 것에는 한계가 존재한다.

