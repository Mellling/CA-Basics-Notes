// [ 스레드와 멀티스레드 ] //////////////////////////////////

// 스레드는 CPU에서 사용되는 하드웨어적 스레드, 프로그램에서 사용되는 소프트웨어적 스레드가 있다.

// ## 하드웨어적 스레드 /////////////////////////////////////

// 스레드를 하드웨어적으로 정의 하면 ‘하나의 코어를 동시에 처리하는 명령어의 단위’이다.

// 1코어 1스레드 : 한 번에 하나의 명령어 처리할 수 있는 CPU

// 2코어 4스레드 : 한 번에 네 개의 명령어를 처리할 수 있는 CPU

// 하나의 코어로 여러 명령어를 동시에 처리할 수 있는 CPU를 멀티스레드 프로세서 또는 멀티스레드 CPU라고 한다.

//////////////////////////////////////////////////////////

// ## 소프트웨어적 스레드 ///////////////////////////////////

// 소프트웨어적으로 정의된 스레드는 ‘하나의 프로그램에서 독립적으로 실행되는 단위’이다.

// 하나의 프로그램은 실행되는 과정에서 한 부분만 실행될 수도 있지만, 프로그램의 여러 부분이 동시에 실행될 수도 있다.

// -사용자로부터 입력받은 내용을 화면에 보여주는 기능
// - 사용자가 입력한 내용이 맞춤법에 맞는지 검사하는 기능
// - 사용자가 입력한 내용을 수시로 저장하는 기능

// 이러한 기능들을 작동시키는 코드를 각각의 스레드로 만들면 동시에 실행할 수 있다.

//////////////////////////////////////////////////////////

// ## 멀티스레드 프로세서 ///////////////////////////////////

// 멀티스레드 프로세서의 가장 큰 핵심은 레지스터인데, 하나의 명령어를 처리하기 위해 꼭 필요한 레지스터를 여러 개 가지고 있으면 된다.

// 프로그램 입장에서 봤을 땐 한 번에 하나의 명령어를 처리하는 CPU가 네 개 있는 것처럼 보인다.그래서 하드웨어 스레드를 논리 프로세서라 부르기도 한다.

// 정리 ---------------------------------------------------
// - 코어는 명령어를 실행할 수 있는 ‘하드웨어 부품’이다.
// - 스레드는 ‘명령어를 실행하는 단위’이다.
// - 멀티코어 프로세서는 명령어를 실행할 수 있는 하드웨어 부품이 CPU 안에 두 개 이상 있는 CPU를 말한다.
// - 멀티스레드 프로세서는 하나의 코어로 여러 개의 명령어를 동시에 실행할 수 있는 CPU를 말한다.
