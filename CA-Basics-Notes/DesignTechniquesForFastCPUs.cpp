// [ 코어와 멀티코어 ] /////////////////////////////////////

// 오늘날의 CPU는 많은 기술적 발전으로 인해 내부에 ‘명령어를 실행하는 부품’을 얼마든지 만들 수 있게 됐다.

// → 따라서 지금까지 알고 있던 CPU의 정의, ‘명령어를 실행하는 부품’은 오늘날의 코어라는 용어로 사용이 된다.

// 코어를 여러 개 포함하고 있는 CPU를 멀티코어 CPU 또는 멀티코어 프로세서라고 부른다.

// CPU의 코어마다 처리할 연산이 적절히 분배되지 않는다면 코어 수에 비례하여 연산 속도가 증가하지 않는다.

// 또한 처리하고자 하는 작업량보다 코어 수가 지나치게 많아도 성능에 크게 영향이 없다.

// 중요한 것은 코어마다 처리할 명령어들을 얼마나 적절하게 분배하느냐이고 그에 따라서 연산 속도는 크게 달라진다.

