// ## 메인보드와 시스템 버스 ////////////////////////////////

// 위의 네 가지의 핵심 부품들은 모두 메인보드(마더보드)라는 판에 연결된다.메인보드에는 이외에도 여러 부품을 부착할 수 있는 슬롯과 연결 단자가 있다.

// 메인보드에 연결된 부품들은 서로 정보를 주고받을 수 있는데, 메인보드 내부에 있는 버스라는 통로 덕분이다.

// 메인보드 안에는 여러 종류의 버스가 존재하는데, 여러 버스 가운데 컴퓨터의 네 가지 핵심 부품을 연결하는 가장 중요한 버스를 시스템 버스라 부른다.

// 시스템 버스는 아래와 같이 구성되어 있다.

// - 주소 버스 : 주소를 주고 받는 통로.
// - 데이터 버스 : 명령어와 데이터를 주고받는 통로.
// - 제어 버스 : 제어 신호를 주고받는 통로.